const NEPALI_NUMBERS = {
  '0': '०', '1': '१', '2': '२', '3': '३',
  '4': '४', '5': '५', '6': '६', '7': '७',
  '8': '८', '9': '९',
};

function toNepaliNum(num) {
  return String(num).split('').map(c => NEPALI_NUMBERS[c] || c).join('');
}

export function toBs(isoDate) {
  const date = new Date(isoDate);
  const refAd = new Date(1943, 3, 14);
  const diffMs = date.getTime() - refAd.getTime();
  let daysDiff = Math.floor(diffMs / (1000 * 60 * 60 * 24));

  let bsYear = 2000;
  let bsMonth = 1;
  let bsDay = 1;

  while (daysDiff > 0) {
    const monthDays = bsData[bsYear]?.[bsMonth - 1] ?? 30;
    if (daysDiff >= monthDays) {
      daysDiff -= monthDays;
      bsMonth++;
      if (bsMonth > 12) {
        bsMonth = 1;
        bsYear++;
      }
    } else {
      bsDay += daysDiff;
      daysDiff = 0;
    }
  }

  return {
    year: bsYear,
    month: bsMonth,
    day: bsDay,
    hour: date.getHours(),
    minute: date.getMinutes(),
  };
}

export function toBsString(isoDate, format = 'full') {
  const bs = toBs(isoDate);
  const npYear = toNepaliNum(bs.year);
  const npDay = toNepaliNum(bs.day);
  const npMonth = bs.month;
  const npHour = toNepaliNum(bs.hour);
  const npMin = toNepaliNum(bs.minute);

  switch (format) {
    case 'short':
      return `${bs.year}-${String(bs.month).padStart(2, '0')}-${String(bs.day).padStart(2, '0')}`;
    case 'date':
      return `${npYear} साल ${NEPALI_MONTHS[npMonth] || ''} ${npDay} गते`;
    case 'date_en':
      return `${bs.year} ${ENGLISH_MONTHS[npMonth] || ''} ${bs.day}`;
    case 'datetime':
      return `${npYear} साल ${NEPALI_MONTHS[npMonth] || ''} ${npDay} गते, ${npHour}:${npMin} बजे`;
    case 'datetime_en': {
      const hour12 = bs.hour % 12 || 12;
      const ampm = bs.hour >= 12 ? 'PM' : 'AM';
      return `${bs.year} ${ENGLISH_MONTHS[npMonth] || ''} ${bs.day}, ${hour12}:${String(bs.minute).padStart(2, '0')} ${ampm}`;
    }
    default:
      return `${npYear} साल ${NEPALI_MONTHS[npMonth] || ''} ${npDay} गते, ${npHour}:${npMin} बजे`;
  }
}

export default { toBs, toBsString };
