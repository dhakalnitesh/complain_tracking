    public static function toBs(Carbon $date): array
    {
        $refAd = Carbon::create(1943, 4, 14, 0, 0, 0);

        $refBsYear = 2000;
        $refBsMonth = 1;
        $refBsDay = 1;

        $daysDiff = $refAd->diffInDays($date->copy()->startOfDay());

        $bsYear = $refBsYear;
        $bsMonth = $refBsMonth;
        $bsDay = $refBsDay;

        while ($daysDiff > 0) {
            $monthDays = self::$bs[$bsYear][$bsMonth - 1] ?? 30;
            if ($daysDiff >= $monthDays) {
                $daysDiff -= $monthDays;
                $bsMonth++;
                if ($bsMonth > 12) {
                    $bsMonth = 1;
                    $bsYear++;
                }
            } else {
                $bsDay += $daysDiff;
                $daysDiff = 0;
            }
        }

        return [
            'year' => $bsYear,
            'month' => $bsMonth,
            'day' => $bsDay,
            'hour' => (int) $date->format('H'),
            'minute' => (int) $date->format('i'),
        ];
    }

    public static function toBsString(Carbon $date, string $format = 'full'): string
    {
        $bs = self::toBs($date);
        $npYear = self::toNepaliNum($bs['year']);
        $npDay = self::toNepaliNum($bs['day']);
        $npMonth = $bs['month'];
        $npHour = self::toNepaliNum($bs['hour']);
        $npMin = self::toNepaliNum($bs['minute']);

        return match ($format) {
            'short' => "{$npYear}-" . str_pad($bs['month'], 2, '0', STR_PAD_LEFT) . '-' . str_pad($bs['day'], 2, '0', STR_PAD_LEFT),
            'date' => "{$npYear} साल " . (self::NEPALI_MONTHS[$npMonth] ?? '') . " {$npDay} गते",
            'date_en' => ($bs['year']) . ' ' . (self::ENGLISH_MONTHS[$npMonth] ?? '') . ' ' . $bs['day'],
            'datetime' => "{$npYear} साल " . (self::NEPALI_MONTHS[$npMonth] ?? '') . " {$npDay} गते, {$npHour}:{$npMin} बजे",
            'datetime_en' => ($bs['year']) . ' ' . (self::ENGLISH_MONTHS[$npMonth] ?? '') . ' ' . $bs['day'] . ', ' . str_pad($bs['hour'], 2, '0', STR_PAD_LEFT) . ':' . str_pad($bs['minute'], 2, '0', STR_PAD_LEFT),
            default => "{$npYear} साल " . (self::NEPALI_MONTHS[$npMonth] ?? '') . " {$npDay} गते, {$npHour}:{$npMin} बजे",
        };
    }

    public static function toNepaliNum(int|string $num): string
    {
        $str = (string) $num;
        $result = '';
        for ($i = 0; $i < strlen($str); $i++) {
            $result .= self::NEPALI_NUMBERS[$str[$i]] ?? $str[$i];
        }
        return $result;
    }

    public static function getMonthNameNp(int $month): string
    {
        return self::NEPALI_MONTHS[$month] ?? '';
    }
